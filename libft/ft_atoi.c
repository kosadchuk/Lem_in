#include "libft.h"

__int128_t		ft_atoi(const char *str)
{
	size_t		i;
	__int128_t	j;
	int			x;

	i = 0;
	j = 0;
	x = 1;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
		x = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] != '\0' && (str[i] >= '0' && str[i] <= '9'))
	{
		j = (j * 10) + (str[i] - '0');
		i++;
	}
	return (j * x);
}
