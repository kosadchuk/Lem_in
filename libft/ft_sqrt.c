#include "libft.h"

int		ft_sqrt(int nb)
{
	int		i;
	int		j;

	i = 1;
	j = 0;
	if (nb <= 0)
		return (0);
	while (i < nb)
	{
		j = i * i;
		if (j == nb)
			break ;
		else
			i++;
	}
	if (i >= nb)
		return (0);
	return (i);
}
