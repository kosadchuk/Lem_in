#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	while (s1[i])
		i++;
	while (j < n)
	{
		s1[i] = s2[j];
		if (s2[j] == '\0')
			return (s1);
		i++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
